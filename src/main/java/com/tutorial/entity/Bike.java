package com.tutorial.entity;

public class Bike {
	
	private Integer bikeId;
	
	private String bikeName;
	
	private String bikeDescription;
	
	private byte[] bikeImg;

	public Integer getBikeId() {
		return bikeId;
	}

	public void setBikeId(Integer bikeId) {
		this.bikeId = bikeId;
	}

	public String getBikeName() {
		return bikeName;
	}

	public void setBikeName(String bikeName) {
		this.bikeName = bikeName;
	}

	public String getBikeDescription() {
		return bikeDescription;
	}

	public void setBikeDescription(String bikeDescription) {
		this.bikeDescription = bikeDescription;
	}

	public byte[] getBikeImg() {
		return bikeImg;
	}

	public void setBikeImg(byte[] bikeImg) {
		this.bikeImg = bikeImg;
	}
	
	

}
